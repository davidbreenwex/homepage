<h1>September 2012 – December 2013: MSc in Computer Science (Conversion), University College Dublin - (Final Grade - First Class Honors 1:1)</h1>
<p>18-month full-time intensive MSc course completing December 2013. Graduated with first class honours.</p>

<p>Below is a list of modules studied in the course, brief descriptions and grades acheived:</p>

<h2>Introduction to database management and information systems <span class="grade">(Grade: A+)</span></h2> 
<p>In this module I was introduced to SQL query language and relational algebra. I also used Microsoft Access to create a small database.</p>

<h2>Computer Systems <span class="grade">(Grade: A+)</span></h2> 
<p>In this module I learned the fundamentals of hardware and software as well as computer maintenance and management. I also learned about virtual machines and virtualisation – I created and linked several virtual machines using both Microsoft Windows and Debian Linux.</p>

<h2>C++ Programming <span class="grade">(Grade: A+)</span></h2> 
<p>In this module I learned how to deal with similar concepts to those which I had learned for C programming, but instead using C++ programming language. In addition I learned the principles of object-oriented programming. In this module I had to write a program to translate a large text file from English to Elvish and back again.</p>

<h2>SQL Programming <span class="grade">(Grade: A+)</span></h2> 
<p>In this module I used and demonstrated my knowledge of SQL by creating an Oracle database system. In addition to this, I also learned additional functionality such as triggers, procedures and PL/SQL.</p>

<h2>Systems Management <span class="grade">(Grade: A+)</span></h2> 
<p>In this module I learned how to manage a computer system and how to set up, link and manage various computers on a network. I also learned the fundamentals of Windows Active Directory, Windows Server 2008 and the OSI reference model.</p>

<h2>Software Engineering Project <span class="grade">(Grade: A+)</span></h2> 
<p>In this project, I developed a metasearch engine which aggregated and returned the aggregated results received from three search engines to the user with a variety of display and search options for the user. I gained extensive experience in php and JavaScript programming languages as well as using third-party API’s. Feel free to visit http://www.davidbreen.eu/blingo.html</p>

<h2>Mobile Application Development <span class="grade">(Grade A+)</span></h2> 
<p>In this module I learned how to develop an Android Application from scratch. I was part of a 7 person team developing the app. My role was to write all of the Java code and set up all the backend functionality in the form of a MySQL database and multiple PHP scripts to query the database and return results. The app is published to the Android Play Store at the following link: https://play.google.com/store/apps/details?id=ie.ucd.csi.android.group9.prezzies</p>

<h2>Information Security <span class="grade">(Grade A+)</span></h2> 
<p>In this module, I learned about the actual processes of information security. I learned about policies, their importance, and how to write effective policies. I also learned about security incident response, as well as penetration testing and the various means by which systems are compromised. </p>

<h2>C Programming <span class="grade">(Grade: B+)</span></h2> 
<p>This module was an introduction to the C programming language. In this module I developed an understanding of C programming using loops, conditionals, file input/output, arrays, records, manual memory management, stacks, linked lists, pointers and functions.</p>

<h2>Internet Web Professionals <span class="grade">(Grade: B+)</span></h2> 
<p>In this module I learned how to use php, html and CSS to create a web page to the current industry standards. I also learned how to set up and implement a MySQL database and use it to store data submitted in forms by a user.</p>

<h2>Professional Java Programming I & II <span class="grade">(Grade C+)</span></h2> 
<p>These modules are aimed at enabling students to gain an understanding of the basics concepts involved in Java programming. The topics covered included the following:  Data  Types &  Operators;  Classes,  Methods  &  Interfaces;  Java  Language Fundamentals; Object Oriented Programming; Execution Flow Control.</p>

